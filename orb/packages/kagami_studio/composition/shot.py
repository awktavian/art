class ShotRenderer:
    """Renders shots using appropriate generators.

    Automatically routes to:
    - Runway: ACTION, REACTION, ESTABLISHING, CUTAWAY
    - HeyGen: DIALOGUE, MONOLOGUE
    - Static: STILL, TITLE, LOWER_THIRD

    Audio is rendered via AudioRenderer with:
    - Voice (TTS)
    - SFX
    - Music
    - Ambient
    """

    def __init__(self):
        self._runway = None
        self._heygen = None
        self._tts = None
        self._audio_renderer = None
        self._initialized = False

    async def initialize(self) -> None:
        """Initialize generators."""
        if self._initialized:
            return

        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

        # Initialize audio renderer
        from kagami_studio.composition.audio import get_audio_renderer

        self._audio_renderer = await get_audio_renderer()

        self._initialized = True
        logger.info("ShotRenderer initialized")

    async def render(self, shot: Shot) -> ShotResult:
        """Render a shot.

        Rendering flow:
        1. Generate video (HeyGen/Runway/Static)
        2. Generate voice audio (TTS)
        3. Mix audio tracks (voice + sfx + music + ambient)
        4. Combine video + audio

        Args:
            shot: Shot definition

        Returns:
            ShotResult with video/audio paths
        """
        if not self._initialized:
            await self.initialize()

        start = time.perf_counter()

        try:
            # Route to appropriate generator
            if shot.needs_heygen:
                result = await self._render_heygen(shot)
            elif shot.needs_runway:
                result = await self._render_runway(shot)
            else:
                result = await self._render_static(shot)

            # If we have additional audio layers, mix them
            if result.success and result.audio_path and (shot.sfx or shot.music or shot.ambient):
                result = await self._mix_shot_audio(shot, result)

            result.render_time_s = time.perf_counter() - start
            return result

        except Exception as e:
            logger.error(f"Shot render failed: {e}")
            return ShotResult(
                success=False,
                error=str(e),
                shot=shot,
                render_time_s=time.perf_counter() - start,
            )

    async def _mix_shot_audio(self, shot: Shot, result: ShotResult) -> ShotResult:
        """Mix additional audio tracks for a shot.

        Uses AudioRenderer to mix:
        - Voice (from TTS)
        - SFX (sound effects)
        - Music (background)
        - Ambient (environmental)
        """
        if not self._audio_renderer:
            return result

        # Resolve SFX paths (would load from asset library)
        sfx_paths = []
        for sfx_name in shot.sfx:
            sfx_path = self._resolve_sfx_path(sfx_name)
            if sfx_path:
                sfx_paths.append(sfx_path)

        # Resolve music/ambient paths
        music_path = self._resolve_music_path(shot.music) if shot.music else None
        ambient_path = self._resolve_ambient_path(shot.ambient) if shot.ambient else None

        # Mix audio
        audio_result = await self._audio_renderer.render_shot_audio(
            voice_path=result.audio_path,
            sfx_paths=sfx_paths if sfx_paths else None,
            music_path=music_path,
            ambient_path=ambient_path,
        )

        if audio_result.success and audio_result.audio_path:
            # Replace audio in video if we have video
            if result.video_path:
                final_video = await self._audio_renderer.combine_video_audio(
                    result.video_path,
                    audio_result.audio_path,
                )
                if final_video:
                    result.video_path = final_video

            result.audio_path = audio_result.audio_path

        return result

    def _resolve_sfx_path(self, sfx_name: str) -> Path | None:
        """Resolve SFX name to file path."""
        # Check common locations
        sfx_dirs = [
            Path("assets/audio/sfx"),
            Path("assets/sfx"),
            Path("/tmp/kagami_sfx"),
        ]

        for sfx_dir in sfx_dirs:
            for ext in [".mp3", ".wav", ".ogg"]:
                path = sfx_dir / f"{sfx_name}{ext}"
                if path.exists():
                    return path

        return None

    def _resolve_music_path(self, music_name: str) -> Path | None:
        """Resolve music name to file path."""
        music_dirs = [
            Path("assets/audio/music"),
            Path("assets/music"),
            Path("/tmp/kagami_music"),
        ]

        for music_dir in music_dirs:
            for ext in [".mp3", ".wav", ".ogg"]:
                path = music_dir / f"{music_name}{ext}"
                if path.exists():
                    return path

        return None

    def _resolve_ambient_path(self, ambient_name: str) -> Path | None:
        """Resolve ambient name to file path."""
        ambient_dirs = [
            Path("assets/audio/ambient"),
            Path("assets/ambient"),
            Path("/tmp/kagami_ambient"),
        ]

        for ambient_dir in ambient_dirs:
            for ext in [".mp3", ".wav", ".ogg"]:
                path = ambient_dir / f"{ambient_name}{ext}"
                if path.exists():
                    return path

        return None

    async def _render_heygen(self, shot: Shot) -> ShotResult:
        """Render dialogue shot with HeyGen Avatar IV."""
        import httpx
        from kagami.core.security import get_secret

        from kagami_studio.characters import load_character

        heygen_key = get_secret("heygen_api_key")
        if not heygen_key:
            return ShotResult(success=False, error="HeyGen API key not found", shot=shot)

        # Get character
        char = None
        image_path = shot.subject_image

        if shot.character:
            char = load_character(shot.character)
            if char and char.avatar.primary_image:
                image_path = char.avatar.primary_image

        if not image_path or not image_path.exists():
            return ShotResult(success=False, error="No image for HeyGen", shot=shot)

        # Generate TTS first
        audio_result = await self._generate_tts(shot)
        if not audio_result.success:
            return ShotResult(success=False, error=f"TTS failed: {audio_result.error}", shot=shot)

        async with httpx.AsyncClient(timeout=120.0) as client:
            # Upload image
            resp = await client.post(
                "https://upload.heygen.com/v1/asset",
                headers={"X-API-KEY": heygen_key, "Content-Type": "image/png"},
                content=image_path.read_bytes(),
            )
            if resp.status_code != 200:
                return ShotResult(
                    success=False, error=f"Image upload failed: {resp.text[:200]}", shot=shot
                )

            image_key = resp.json().get("data", {}).get("image_key")

            # Upload audio
            resp = await client.post(
                "https://upload.heygen.com/v1/asset",
                headers={"X-API-KEY": heygen_key, "Content-Type": "audio/mpeg"},
                content=audio_result.audio_path.read_bytes(),
            )
            if resp.status_code != 200:
                return ShotResult(
                    success=False, error=f"Audio upload failed: {resp.text[:200]}", shot=shot
                )

            audio_url = resp.json().get("data", {}).get("url")

            # Submit Avatar IV job
            payload = {
                "image_key": image_key,
                "video_title": f"shot_{int(time.time())}",
                "video_orientation": "landscape",
                "fit": "contain",
                "audio_url": audio_url,
                "custom_motion_prompt": f"{shot.motion} expression, {shot.camera.value} framing",
                "enhance_custom_motion_prompt": True,
            }

            resp = await client.post(
                "https://api.heygen.com/v2/video/av4/generate",
                headers={"X-Api-Key": heygen_key, "Content-Type": "application/json"},
                json=payload,
            )

            if resp.status_code != 200:
                return ShotResult(
                    success=False, error=f"HeyGen submit failed: {resp.text[:200]}", shot=shot
                )

            video_id = resp.json().get("data", {}).get("video_id")

            # Poll for completion
            video_url = None
            for _ in range(120):  # 10 minute timeout
                resp = await client.get(
                    f"https://api.heygen.com/v1/video_status.get?video_id={video_id}",
                    headers={"X-Api-Key": heygen_key},
                )
                data = resp.json().get("data", {})
                status = data.get("status")

                if status == "completed":
                    video_url = data.get("video_url")
                    break
                elif status == "failed":
                    return ShotResult(
                        success=False, error=f"HeyGen failed: {data.get('error')}", shot=shot
                    )

                await asyncio.sleep(5)

            if not video_url:
                return ShotResult(success=False, error="HeyGen timeout", shot=shot)

            # Download video
            resp = await client.get(video_url)
            video_path = OUTPUT_DIR / f"heygen_{video_id}.mp4"
            video_path.write_bytes(resp.content)

            # Get duration
            duration = self._get_video_duration(video_path)

            return ShotResult(
                success=True,
                video_path=video_path,
                audio_path=audio_result.audio_path,
                duration_s=duration,
                shot=shot,
                generator_used="heygen",
            )

