"""Unified Shot — Single source of truth for production units.

A Shot is the atomic unit of video production. It represents one continuous
piece of footage, whether generated by:
- Runway (action, establishing, cutaway)
- HeyGen (dialogue with lip sync)
- Static image with Ken Burns effect
- Screen recording
- Camera capture

This replaces:
- effectors/video/hybrid_composer.py Shot, ShotType
- kagami_media/production/shot.py Shot, ShotResult, ShotRenderer
- core/media/production/scene.py Beat (similar concept)
"""

from __future__ import annotations

import asyncio
import logging
import subprocess
import time
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

OUTPUT_DIR = Path("/tmp/kagami_shots")


class ShotType(str, Enum):
    """Type of shot — determines which generator to use.

    Based on TED Talk cinematography and Roger Deakins principles:
    - Multi-camera coverage (wide, medium, close)
    - Purposeful movement serves narrative
    - Balanced composition with environmental context
    """

    # === Front/Speaker shots (TED Talk standard) ===
    FRONT_WIDE = "front_wide"  # Full stage, speaker + backdrop + slides
    FRONT_MEDIUM = "front_medium"  # Waist up, gestures visible
    FRONT_CLOSEUP = "front_closeup"  # Face only, emotions + nuance
    FRONT_EXTREME_CLOSEUP = "front_xcu"  # Eyes/detail for emphasis

    # === HeyGen shots (requires face, lip sync) ===
    DIALOGUE = "dialogue"  # Character speaking (default front medium)
    MONOLOGUE = "monologue"  # Extended speaking

    # === Audience/Reverse shots (back of speaker, crowd views) ===
    AUDIENCE = "audience"  # View OF the audience (wide)
    AUDIENCE_CLOSEUP = "audience_closeup"  # Single audience member reacting
    REVERSE = "reverse"  # Back of speaker's head, facing audience
    OVER_SHOULDER_AUDIENCE = "ots_audience"  # Over speaker's shoulder to crowd
    POV_AUDIENCE = "pov_audience"  # From audience perspective at speaker
    REACTION_AUDIENCE = "reaction_audience"  # Audience reacting

    # === Two-shots and coverage ===
    TWO_SHOT = "two_shot"  # Speaker + interviewer/moderator
    WALKING = "walking"  # Speaker walking/pacing (Deakins tracking)
    PROFILE = "profile"  # Side view of speaker

    # === Runway shots (any subject, no lip sync) ===
    ACTION = "action"  # Subject in motion
    REACTION = "reaction"  # Subject reacting
    ESTABLISHING = "establishing"  # Scene-setting wide (venue exterior)
    CUTAWAY = "cutaway"  # B-roll, detail (hands, notes, screen)
    TRANSITION = "transition"  # Crossfade/wipe

    # === Static/simple shots ===
    STILL = "still"  # Static image
    TITLE = "title"  # Text on screen
    LOWER_THIRD = "lower_third"  # Name/title card
    SLIDES = "slides"  # Full-screen presentation slides


class CameraAngle(str, Enum):
    """Camera framing — Roger Deakins inspired.

    Principles:
    - Wider lenses (32mm, 40mm) for environmental context
    - Balanced, geometric composition
    - Movement only when narrative requires it
    """

    # === Standard framing (TED Talk coverage) ===
    EXTREME_WIDE = "extreme_wide"  # Full venue/stage
    WIDE = "wide"  # Full speaker + context
    MEDIUM_WIDE = "medium_wide"  # Knees up
    MEDIUM = "medium"  # Waist up (most common)
    MEDIUM_CLOSE = "medium_close"  # Chest up
    CLOSE = "close"  # Head and shoulders
    EXTREME_CLOSE = "extreme_close"  # Face detail

    # === Angle variations ===
    FRONT = "front"  # Direct front (0°)
    THREE_QUARTER_FRONT = "three_quarter_front"  # 45° from front
    PROFILE = "profile"  # Side view (90°)
    THREE_QUARTER_BACK = "three_quarter_back"  # 135° (see some face)
    BEHIND = "behind"  # Full back view (180°)

    # === Special angles ===
    OVER_SHOULDER = "over_shoulder"  # OTS standard
    POV = "pov"  # Point of view
    HIGH_ANGLE = "high_angle"  # Looking down (vulnerability)
    LOW_ANGLE = "low_angle"  # Looking up (power)
    DUTCH = "dutch"  # Tilted (tension)
    CROWD_WIDE = "crowd_wide"  # Wide shot of audience
    BIRDS_EYE = "birds_eye"  # Directly overhead

    # === Movement ===
    TRACKING = "tracking"  # Following subject movement
    DOLLY = "dolly"  # Push in/pull out
    CRANE = "crane"  # Vertical movement


@dataclass
class Shot:
    """Universal production unit.

    Attributes:
        type: Shot type (determines generator)

        # Content (provide one)
        text: Dialogue text (for DIALOGUE/MONOLOGUE)
        action_prompt: Motion description (for ACTION/REACTION/ESTABLISHING)

        # Subject
        character: Character identity_id (loads from metadata.json)
        subject_image: Path to image (for non-character subjects)

        # Audio
        sfx: Sound effect names
        music: Background music cue
        ambient: Ambient sound

        # Framing
        camera: Camera angle
        duration_s: Target duration
        motion: Emotional direction

        # Voice (ElevenLabs)
        voice_remix: Voice remix description (e.g., "warmer British accent")
        voice_remix_strength: How much to modify voice (0.1-1.0)
        voice_id: Optional specific voice_id to use (for remixed variants)

        # Effects
        color_grade: Color grading preset
        vignette: Vignette strength (0-1)
        grain: Film grain amount (0-1)
    """

    # Shot type
    type: ShotType = ShotType.DIALOGUE

    # Content (at least one required)
    text: str | None = None
    action_prompt: str | None = None

    # Subject
    character: str | None = None
    subject_image: Path | None = None

    # Audio layers
    sfx: list[str] = field(default_factory=list)
    music: str | None = None
    ambient: str | None = None

    # Framing
    camera: CameraAngle = CameraAngle.MEDIUM
    duration_s: float = 5.0
    motion: str = "natural"

    # Voice (ElevenLabs remixing)
    voice_remix: str | None = None  # e.g., "warmer British accent"
    voice_remix_strength: float = 0.5  # 0.1-1.0
    voice_id: str | None = None  # Override voice_id for pre-remixed variants

    # Effects
    color_grade: str | None = None
    vignette: float = 0.0
    grain: float = 0.0

    # Metadata
    id: str | None = None

    def __post_init__(self):
        """Validate shot configuration."""
        if self.type in (ShotType.DIALOGUE, ShotType.MONOLOGUE):
            if not self.text:
                raise ValueError(f"{self.type.value} shot requires text")
            if not self.character and not self.subject_image:
                raise ValueError(f"{self.type.value} shot requires character or subject_image")

        elif self.type in (
            ShotType.ACTION,
            ShotType.REACTION,
            ShotType.ESTABLISHING,
            ShotType.CUTAWAY,
        ):
            if not self.action_prompt:
                raise ValueError(f"{self.type.value} shot requires action_prompt")

    @property
    def needs_runway(self) -> bool:
        """Whether this shot should use Runway (motion video)."""
        return self.type in (
            # Action/B-roll
            ShotType.ACTION,
            ShotType.REACTION,
            ShotType.ESTABLISHING,
            ShotType.CUTAWAY,
            ShotType.WALKING,
            # Audience shots
            ShotType.AUDIENCE,
            ShotType.AUDIENCE_CLOSEUP,
            ShotType.REVERSE,
            ShotType.OVER_SHOULDER_AUDIENCE,
            ShotType.POV_AUDIENCE,
            ShotType.REACTION_AUDIENCE,
        )

    @property
    def needs_heygen(self) -> bool:
        """Whether this shot should use HeyGen (lip sync)."""
        return self.type in (ShotType.DIALOGUE, ShotType.MONOLOGUE)

    @property
    def needs_tts(self) -> bool:
        """Whether this shot needs TTS audio."""
        return self.text is not None

    def to_dict(self) -> dict[str, Any]:
        """Serialize to dictionary."""
        return {
            "type": self.type.value,
            "text": self.text,
            "action_prompt": self.action_prompt,
            "character": self.character,
            "subject_image": str(self.subject_image) if self.subject_image else None,
            "sfx": self.sfx,
            "music": self.music,
            "ambient": self.ambient,
            "camera": self.camera.value,
            "duration_s": self.duration_s,
            "motion": self.motion,
            "voice_remix": self.voice_remix,
            "voice_remix_strength": self.voice_remix_strength,
            "voice_id": self.voice_id,
            "color_grade": self.color_grade,
            "vignette": self.vignette,
            "grain": self.grain,
            "id": self.id,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> Shot:
        """Deserialize from dictionary."""
        return cls(
            type=ShotType(data.get("type", "dialogue")),
            text=data.get("text"),
            action_prompt=data.get("action_prompt"),
            character=data.get("character"),
            subject_image=Path(data["subject_image"]) if data.get("subject_image") else None,
            sfx=data.get("sfx", []),
            music=data.get("music"),
            ambient=data.get("ambient"),
            camera=CameraAngle(data.get("camera", "medium")),
            duration_s=data.get("duration_s", 5.0),
            motion=data.get("motion", "natural"),
            voice_remix=data.get("voice_remix"),
            voice_remix_strength=data.get("voice_remix_strength", 0.5),
            voice_id=data.get("voice_id"),
            color_grade=data.get("color_grade"),
            vignette=data.get("vignette", 0.0),
            grain=data.get("grain", 0.0),
            id=data.get("id"),
        )


@dataclass
class ShotResult:
    """Result of rendering a shot."""

    success: bool
    video_path: Path | None = None
    audio_path: Path | None = None
    duration_s: float = 0.0
    render_time_s: float = 0.0
    shot: Shot | None = None
    error: str | None = None

    # Generation metadata
    generator_used: str = ""  # runway, heygen, static, etc.
    cost_cents: float = 0.0


